class Resource {

    // 15 Instance Methods
    void use() { System.out.println("Resource is used."); }
    void conserve() { System.out.println("Resource is conserved."); }
    void recycle() { System.out.println("Resource is recycled."); }
    void renew() { System.out.println("Resource is renewed."); }
    void distribute() { System.out.println("Resource is distributed."); }
    void collect() { System.out.println("Resource is collected."); }
    void store() { System.out.println("Resource is stored."); }
    void transport() { System.out.println("Resource is transported."); }
    void protect() { System.out.println("Resource is protected."); }
    void manage() { System.out.println("Resource is managed."); }
    void monitor() { System.out.println("Resource is monitored."); }
    void allocate() { System.out.println("Resource is allocated."); }
    void develop() { System.out.println("Resource is developed."); }
    void maintain() { System.out.println("Resource is maintained."); }
    void displayInfo() { System.out.println("Resource information displayed."); }

    // 5 Static Methods
    static void resourceType() { System.out.println("Resources can be renewable or non-renewable."); }
    static void naturalResource() { System.out.println("Water and air are natural resources."); }
    static void importance() { System.out.println("Resources are essential for life."); }
    static void scarcityInfo() { System.out.println("Resources can become scarce."); }
    static void sustainability() { System.out.println("Sustainable use preserves resources."); }
}