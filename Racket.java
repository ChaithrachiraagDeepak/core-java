class Racket{

    String brand;
    String sportType;
    double weight;
    String gripType;
    String color;
    double price;
    String material;
    int length;
    boolean isProfessional;
    int warrantyYears;

    Racket(String brand, String sportType, double weight,String gripType, String color, double price,
           String material, int length,boolean isProfessional, int warrantyYears){

        this.brand = brand;
        this.sportType = sportType;
        this.weight = weight;
        this.gripType = gripType;
        this.color = color;
        this.price = price;
        this.material = material;
        this.length = length;
        this.isProfessional = isProfessional;
        this.warrantyYears = warrantyYears;
    }
}