class Blanket{

    String brand;
    String material;
    String color;
    double price;
    String size;
    boolean isElectric;

    Blanket(String brand, String material, String color,double price, String size, boolean isElectric) {

        this.brand = brand;
        this.material = material;
        this.color = color;
        this.price = price;
        this.size = size;
        this.isElectric = isElectric;
    }
}