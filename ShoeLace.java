class ShoeLace{

    String brand;
    String color;
    double length;
    String material;
    double price;
    boolean isElastic;
    String size;
    String pattern;
    int stock;
    String quality;

    ShoeLace(String brand, String color, double length, String material,
             double price, boolean isElastic, String size,
             String pattern, int stock, String quality){

        this.brand = brand;
        this.color = color;
        this.length = length;
        this.material = material;
        this.price = price;
        this.isElastic = isElastic;
        this.size = size;
        this.pattern = pattern;
        this.stock = stock;
        this.quality = quality;
    }
}