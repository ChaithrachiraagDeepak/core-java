class TennisBall {

    // 15 Instance Methods
    void bounce() {
        System.out.println("Tennis ball is bouncing.");
    }

    void roll() {
        System.out.println("Tennis ball is rolling.");
    }

    void hit() {
        System.out.println("Tennis ball is hit by the racket.");
    }

    void spin() {
        System.out.println("Tennis ball is spinning.");
    }

    void serve() {
        System.out.println("Tennis ball is served.");
    }

    void smash() {
        System.out.println("Tennis ball is smashed.");
    }

    void lob() {
        System.out.println("Tennis ball is lobbed high.");
    }

    void drop() {
        System.out.println("Tennis ball drops on the court.");
    }

    void crossCourt() {
        System.out.println("Tennis ball goes cross court.");
    }

    void netTouch() {
        System.out.println("Tennis ball touches the net.");
    }

    void out() {
        System.out.println("Tennis ball goes out of the court.");
    }

    void in() {
        System.out.println("Tennis ball lands inside the court.");
    }

    void getWet() {
        System.out.println("Tennis ball gets wet in rain.");
    }

    void wearOut() {
        System.out.println("Tennis ball is worn out.");
    }

    void showDetails() {
        System.out.println("Tennis ball details displayed.");
    }

    // 5 Static Methods
    static void materialInfo() {
        System.out.println("Tennis ball is made of rubber and felt.");
    }

    static void weightInfo() {
        System.out.println("Standard tennis ball weighs around 58 grams.");
    }

    static void colorInfo() {
        System.out.println("Tennis ball is usually yellow.");
    }

    static void usedInSport() {
        System.out.println("Tennis ball is used in lawn tennis.");
    }

    static void bounceStandard() {
        System.out.println("Tennis ball has standard bounce height.");
    }
}