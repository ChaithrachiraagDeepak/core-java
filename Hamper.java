class Hamper {

    // 15 Instance Methods
    void open() {
        System.out.println("Hamper is opened.");
    }

    void close() {
        System.out.println("Hamper is closed.");
    }

    void addItem() {
        System.out.println("Item added to the hamper.");
    }

    void removeItem() {
        System.out.println("Item removed from the hamper.");
    }

    void checkItems() {
        System.out.println("Checking items in the hamper.");
    }

    void wrap() {
        System.out.println("Hamper is being wrapped.");
    }

    void decorate() {
        System.out.println("Hamper is decorated beautifully.");
    }

    void deliver() {
        System.out.println("Hamper is delivered.");
    }

    void packFruits() {
        System.out.println("Fruits are packed in the hamper.");
    }

    void packChocolates() {
        System.out.println("Chocolates are packed in the hamper.");
    }

    void packGifts() {
        System.out.println("Gifts are packed in the hamper.");
    }

    void weigh() {
        System.out.println("Hamper weight is measured.");
    }

    void seal() {
        System.out.println("Hamper is sealed.");
    }

    void displayItems() {
        System.out.println("Displaying hamper items.");
    }

    void giftWrap() {
        System.out.println("Hamper is gift wrapped.");
    }

    // 5 Static Methods
    static void hamperType() {
        System.out.println("Hamper can be gift, fruit, or festive type.");
    }

    static void priceRange() {
        System.out.println("Hamper price starts from 1000.");
    }

    static void materialInfo() {
        System.out.println("Hamper is made of basket or box material.");
    }

    static void occasionInfo() {
        System.out.println("Hamper is used for special occasions.");
    }

    static void discountOffer() {
        System.out.println("Festival discount available on hampers.");
    }
}